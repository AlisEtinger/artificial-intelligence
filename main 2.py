import turtle
t =turtle.Turtle('turtle')
t.speed(10)
t.penup()
t.goto(-30,0)
t.color(255,0,0)
t.fillcolor(255,0,0)
t.pendown()
t.begin_fill()
t.circle(60)
t.end_fill()
t.penup()
t.color(0,255,0)
t.fillcolor(0,255,0)
t.goto(-60,0)
t.pendown()
t.begin_fill()
t.circle(60)
t.end_fill()
t.penup()
t.color(0,0,255)
t.fillcolor(0,0,255)
t.goto(-90,0)
t.pendown()
t.begin_fill()
t.circle(60)
t.end_fill()
t.goto(-90,30)